/******************************************************************************
* \Syntax          : void BVM_Read(void)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void BVM_Read(void)
{
uint32 temp;
uint8 i=0,j=0;
BATTERY_READS[Read_Index]=ADC_VAL;
Read_Index++;
if (Read_Index==5)
{
	for (i=0;i<5;i++)
	{
		BATTERY_SORTED_READS[i]=BATTERY_READS[i];
	}
	for (i=0;i<4;i++)
	{
		for(j=0;j<5;j++)
		{
			if(BATTERY_SORTED_READS[i]>BATTERY_SORTED_READS[j])
			{
				temp=BATTERY_SORTED_READS[i];
				BATTERY_SORTED_READS[i]=BATTERY_SORTED_READS[j];
				BATTERY_SORTED_READS[j]=temp;
			}
		}
	}
	Read_Index=0;
	BVM=(BATTERY_SORTED_READS[3]*88)/37;
}
}

